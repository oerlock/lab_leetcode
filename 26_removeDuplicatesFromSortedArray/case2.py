def test_solution():
    s = Solution()
    test_input_output = [
        ([1, 1, 2], 2, [1, 2]),
        ([0, 0, 1, 1, 1, 2, 2, 3, 3, 4], 5, [0, 1, 2, 3, 4]),
    ]
    for i in test_input_output:
        rs = s.removeDuplicates(i[0])
        assert rs == i[1]
        assert i[0][:rs] == i[2]
